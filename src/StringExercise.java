public class StringExercise {
    public static void main(String[] args) {
        String string0 = "We don't need no education\nWe don't need no thought control";
        System.out.println(string0);
        String string1 = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(string1);
        String string2 = "In windows, the main drive is usually C:\\";
        System.out.println(string2);
        String string3 = "I can do backslashes \\, double backslashes \\\\,\nand the amazing triple backslash \\\\\\!";
        System.out.println(string3);
    }
}
